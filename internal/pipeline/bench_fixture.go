package pipeline

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

// GenerateSyntheticRepo writes a tree of N pure-Go files with deterministic
// symbols + calls into dir. Exported so benchmarks and doctor fixtures share
// one generator.
//
// Each file has ~11 symbols (one type, one constructor, 8 methods, one chain
// function) and each method calls two constructors in other files by
// qualified name, so ref resolution has real work to do.
//
// symbolsTarget is a best-effort target; the real count is
// ceil(symbolsTarget/10) * 11. On a 10k target we emit ~1000 files.
func GenerateSyntheticRepo(dir string, symbolsTarget int) (files int, err error) {
	return GenerateSyntheticRepoOpts(dir, symbolsTarget, 2)
}

// GenerateSyntheticRepoOpts is GenerateSyntheticRepo with a fanout knob.
//
// The method→constructor calls form a bipartite graph with diameter ~2 —
// useless for traversal benchmarks. The per-file Chain functions fix that:
// Chain{i} calls `fanout` other files' Chain functions, so the call graph
// has unbounded diameter and tunable density. fanout 2 is the sparse
// default; 8 approximates a dense, highly intertwined codebase.
func GenerateSyntheticRepoOpts(dir string, symbolsTarget, fanout int) (files int, err error) {
	if symbolsTarget <= 0 {
		return 0, fmt.Errorf("symbolsTarget must be > 0")
	}
	if fanout < 1 {
		return 0, fmt.Errorf("fanout must be >= 1")
	}
	files = (symbolsTarget + 9) / 10
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return 0, err
	}
	for i := 0; i < files; i++ {
		body := renderFile(i, files, fanout)
		path := filepath.Join(dir, fmt.Sprintf("pkg%04d.go", i))
		if err := os.WriteFile(path, []byte(body), 0o644); err != nil {
			return 0, err
		}
	}
	return files, nil
}

// chainStep returns the target offset for the j-th outbound edge of a
// Chain function. j==0 pins step 1 so Chain{i} → Chain{i+1} always
// exists and the graph stays connected; larger j jump by a prime
// stride so deep walks fan out across the whole fixture instead of a
// narrow contiguous band.
func chainStep(j int) int {
	if j == 0 {
		return 1
	}
	return j*173 + 1
}

// renderFile produces one Go source file with a deterministic set of
// symbols and cross-file calls. Using a per-file package name keeps
// qualified names unique across the fixture (and mimics a monorepo).
func renderFile(i, total, fanout int) string {
	var b strings.Builder
	pkg := fmt.Sprintf("pkg%04d", i)
	b.WriteString("// Code generated by pipeline.GenerateSyntheticRepo; DO NOT EDIT.\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)

	fmt.Fprintf(&b, "type Kind%d struct {\n\tid int\n\tname string\n}\n\n", i)
	fmt.Fprintf(&b, "// NewKind%d builds a Kind%d with the given id.\nfunc NewKind%d(id int) *Kind%d { return &Kind%d{id: id} }\n\n", i, i, i, i, i)

	for m := 0; m < 8; m++ {
		// Cross-file call targets: wrap-around modulo keeps refs pointing
		// at real symbols even at the edges of the fixture.
		target1 := (i + 1 + m) % total
		target2 := (i + 2 + m*3) % total
		fmt.Fprintf(&b, "// Action%d_%d exercises cross-file calls.\n", i, m)
		fmt.Fprintf(&b, "func (k *Kind%d) Action%d_%d() int {\n", i, i, m)
		fmt.Fprintf(&b, "\tx := pkg%04d.NewKind%d(k.id)\n", target1, target1)
		fmt.Fprintf(&b, "\ty := pkg%04d.NewKind%d(k.id + 1)\n", target2, target2)
		fmt.Fprintf(&b, "\t_ = x\n\t_ = y\n")
		fmt.Fprintf(&b, "\treturn k.id\n}\n\n")
	}

	fmt.Fprintf(&b, "// Chain%d participates in a deep cross-file call chain.\n", i)
	fmt.Fprintf(&b, "func Chain%d() int {\n\tn := %d\n", i, i)
	for j := 0; j < fanout; j++ {
		t := (i + chainStep(j)) % total
		if t == i {
			continue
		}
		fmt.Fprintf(&b, "\tn += pkg%04d.Chain%d()\n", t, t)
	}
	b.WriteString("\treturn n\n}\n")
	return b.String()
}
